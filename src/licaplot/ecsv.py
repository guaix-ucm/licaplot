def add_args(parser: ArgumentParser) -> None:
    subparser = parser.add_subparsers(dest="command")
    parser_calib = subparser.add_parser("calibration", help="Photodiode calibration data CSV -> ECSV")
    parser_readings = subparser.add_parser("readings", help="Photodiode readings CSV -> ECSV")
    parser_responsivity = subparser.add_parser("responsivity", help="PlotDigitied Manufacturer Responsivity table CSV -> ECSV")

    # Readings Parser
    redex1 = parser_readings.add_mutually_exclusive_group(required=True)
    redex1.add_argument(
        "-a",
        "--scan",
        type=vfile,
        metavar="<CSV>",
        help="CSV was generated by LabView Scan utility",
    )
    redex1.add_argument(
        "-t",
        "--testbench",
        type=vfile,
        metavar="<CSV>",
        help="CSV was manually generated using LabView TestBench utility",
    )

    parser_responsivity.add_argument(
        "-i",
        "--input_file",
        type=vfile,
        metavar="<CSV>",
        help="CSV was manually generated using LabView TestBench utility",
    )
    parser_responsivity.add_argument(
        "-x",
        "--x",
        type=str,
        metavar="<X>",
        default="Wavelength", 
        help="X Column name (defaults to %(default)s)",
    )
    parser_responsivity.add_argument(
        "-y",
        "--y",
        type=str,
        metavar="<Y>",
        default="Responsivity",
        help="Y Column name (defaults to %(default)s)",
    )
    respex1 = parser_responsivity.add_mutually_exclusive_group()
    respex1.add_argument(
        "-n",
        "--normalized",
        action="store_true",
        help="Responsivity is normalized to 1.0",
    )
    respex1.add_argument(
        "-q",
        "--qe",
        action="store_true",
        help="Add Quantum Efficiency data to ECSV",
    )
    parser_responsivity.add_argument(
        "--delimiter",
        type=str,
        default=",",
        help="CSV delimiter (defaults to %(default)s)",
    )

# ================
# MAIN ENTRY POINT
# ================


COMMAND_TABLE = {
    "calibration": calibration_to_ecsv,
    "readings": readings_to_ecsv,
    "responsivity": responsivity_to_ecsv,
}


def ecsv(args: Namespace):
    COMMAND_TABLE[args.command](args)


def main():
    execute(
        main_func=ecsv,
        add_args_func=add_args,
        name=__name__,
        version=__version__,
        description="Convert CSV files to Astropy ECSV files",
    )
