def plotdigitizer_to_ecsv(path: str, delimiter: str, x: str, y: str) -> Table:
    log.info("Converting PlotDigitizer generated %s, %s CSV to Table", x, y)
    table = astropy.io.ascii.read(
            path,
            delimiter=delimiter,
            data_start=1,
            names=(x, y),
            converters={x: np.float64, y: np.float64},
    )
    return table


def quantum_efficiency(table: Table) -> np.ndarray:
    """Computes the Quantum Efficiency given the Responsivity in A/W"""
    K = (const.h * const.c) / const.e
    return  (K * table["Responsivity"]  / table["Wavelength"].to(u.m)).to(u.dimensionless_unscaled)


def responsivity_to_ecsv(args: Namespace) -> None:
    table = plotdigitizer_to_ecsv(args.input_file, args.delimiter, args.x, args.y)
    table[args.x] = np.round(table[args.x], 0) * u.nm
    if args.normalized:
        table[args.y] = table[args.y] * u.dimensionless_unscaled
    else:
        table[args.y] = table[args.y] * u.ampere / u.watt
        if args.qe:
            table['QE'] = quantum_efficiency(table)
    log.info("Generated table is\n%s", table.info)
    output_path, _ = os.path.splitext(args.input_file)
    output_path += ".ecsv"
    log.info("Generating %s", output_path)
    table.write(output_path, overwrite=True)


def calibration_to_ecsv(args: Namespace) -> None:
    table = astropy.io.ascii.read(args.input_file, delimiter=";")
    log.info(table.info)


def readings_to_ecsv(args: Namespace) -> None:
    path = args.scan if args.scan else args.testbench
    if args.scan:
        log.info("Converting AutoScan CSV to Table")
        table = astropy.io.ascii.read(
            path,
            delimiter="\t",
            names=("Index", "Wavelength", "Current"),
        )
        table.remove_column("Index")
        table["Wavelength"] = np.round(table["Wavelength"], 0) * u.nm
        table["Current"] = table["Current"] * u.ampere
    else:
        log.info("Converting TestBench CSV to Table")
        table = astropy.io.ascii.read(
            path,
            delimiter=";",
            data_start=1,
            names=("Wavelength", "Current", "Read Noise"),
            converters={"Wavelength": np.float64, "Current": np.float64, "Read Noise": np.float64},
        )
        table["Wavelength"] = np.round(table["Wavelength"], 0) * u.nm
        table["Current"] = table["Current"] * u.ampere
        table["Read Noise"] = table["Read Noise"] * u.ampere
    log.info("Generated table is\n%s", table.info)
    output_path, _ = os.path.splitext(path)
    output_path += ".ecsv"
    log.info("Generating %s", output_path)
    table.write(output_path, overwrite=True)


